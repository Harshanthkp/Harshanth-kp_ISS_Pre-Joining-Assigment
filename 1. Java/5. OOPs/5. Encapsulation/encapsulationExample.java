// The process by which data (variables) and the code that acts upon them (methods) are integrated as a single unit

public class Encapsulation{  
	//declaring private data member  

	private String name;  

	//defining getter method for data member
  	public String getName(){  
	return name;  
	}  
		//defining setter method for name  
	public void setName(String name){  
		this.name=name;
	}  
}  
